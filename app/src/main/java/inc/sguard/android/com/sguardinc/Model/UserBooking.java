package inc.sguard.android.com.sguardinc.Model;

public class UserBooking {
    String Current_Booking;
    String Current_Booking_Number;

    public UserBooking() {
    }

    public String getCurrent_Booking() {
        return Current_Booking;
    }

    public void setCurrent_Booking(String current_Booking) {
        Current_Booking = current_Booking;
    }

    public String getCurrent_Booking_Number() {
        return Current_Booking_Number;
    }

    public void setCurrent_Booking_Number(String current_Booking_Number) {
        Current_Booking_Number = current_Booking_Number;
    }

}
