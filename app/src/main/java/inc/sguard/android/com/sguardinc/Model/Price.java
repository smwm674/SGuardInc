package inc.sguard.android.com.sguardinc.Model;

public class Price {
    double BaseFair, PerHour, PerMile;

    public Price() {
    }

    public double getBaseFair() {
        return BaseFair;
    }

    public void setBaseFair(double baseFair) {
        BaseFair = baseFair;
    }

    public double getPerHour() {
        return PerHour;
    }

    public void setPerHour(double perHour) {
        PerHour = perHour;
    }

    public double getPerMile() {
        return PerMile;
    }

    public void setPerMile(double perMile) {
        PerMile = perMile;
    }
}
