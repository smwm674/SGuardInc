package inc.sguard.android.com.sguardinc.Model;

public class Verification {
    String driving_back, driving_front, id_back, id_front;

    public Verification() {
    }

    public String getDriving_back() {
        return driving_back;
    }

    public void setDriving_back(String driving_back) {
        this.driving_back = driving_back;
    }

    public String getDriving_front() {
        return driving_front;
    }

    public void setDriving_front(String driving_front) {
        this.driving_front = driving_front;
    }

    public String getId_back() {
        return id_back;
    }

    public void setId_back(String id_back) {
        this.id_back = id_back;
    }

    public String getId_front() {
        return id_front;
    }

    public void setId_front(String id_front) {
        this.id_front = id_front;
    }
}
